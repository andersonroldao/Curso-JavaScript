const subtraiDoisNumeros = function subtrai(a, b) {
    return a - b;
}

console.log(subtraiDoisNumeros(10, 3));


// Funções Anônimas - oculta o nome da function quando está dentro de uma variável
const subtraiTresNumeros = function (a, b, c) {
    return a - b - c;
}

console.log(subtraiTresNumeros(15, 3, 6));