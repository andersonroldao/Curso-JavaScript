/*
+ (adição),
- (subtração),
* (multiplicação),
/ (divisão),
** (exponenciação ou potenciação),
++ (incremento),
-- (decremento).
*/

let numero = 5;

console.log(numero + numero); // 5+5=10
console.log(numero - numero); // 5-5=0
console.log(numero * numero); // 5*5=25
console.log(numero / numero); // 5/5=1
console.log(numero ** numero); // 5*5*5*5*5=3125
console.log(numero++); // incrementa na próxima chamada
console.log(numero--); // decrementa na próxima chamada
console.log(++numero); // incrementa nessa chamada
console.log(--numero); // decrementa nessa chamada